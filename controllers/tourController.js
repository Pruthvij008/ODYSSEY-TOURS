exports.createTour = catchAsync(async (req, res, next) => {
  const doc = await Tour.create(req.body);

  const users = await news.find();
  const emailpromises = users.map(async user => {
    const url = `http://127.0.0.1/:3000/tours/${doc.slug}`;
    const emailins = new Email(user, url);
    return emailins.sendNoti(doc);
  });

  await Promise.all(emailpromises);

  res.status(201).json({
    status: 'success',
    data: {
      data: doc
    }
  });
});

